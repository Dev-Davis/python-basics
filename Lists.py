# Lists are like arrays in Javascript

# Creating a list with multiple types
our_list = ["Michael", "Randy", "Jermaine", 1, 2, 3, True, False]
print(our_list)

# list example with a list as an index
new_list = ["Sean", "Nae", ["Queinton", "Korey"], "Kortney"]
print(new_list)

